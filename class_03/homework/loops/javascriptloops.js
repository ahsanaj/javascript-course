// Exercises: JavaScript loops

// Print every number from 0 to 10

for (var i = 0; i <= 10; i += 1) {
    console.log(i);
}

// Print every number from 4 to -16

for (var j = 4; j >= -16; j -= 1) {
    console.log(j);
}

// Print every fifth number from 8 to 41

for (var k = 8; k <= 41; k += 5) {
    console.log(k);
}