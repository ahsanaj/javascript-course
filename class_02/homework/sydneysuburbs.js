// Depending on the answer, print an appropriate response of your choosing (e.g. maybe what team they might support, what activities the user might engage in etc.)

var userSuburb = "St Mary's";

if (userSuburb === "Sydney CBD") {
    console.log("Don't forget to see Sydney Opera House and Harbour Bridge");
} else if (userSuburb === "St Mary's") {
    console.log("You must be Penrith Panthers' fan.");
} else if (userSuburb === "Canterbury") {
    console.log("Go Bulldogs!");
}